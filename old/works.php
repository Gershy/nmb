									Again the lights wavered briefly.
								</p>
								<p>
									Felix was too distracted to question this flickering. Six months ago he and Carolyn had been flying to Warsaw to rescue yet another cache of books. They'd been debating the novel ERR limits when Stephen had told them to keep it down. Felix had introduced Carolyn to Stephen and she'd taken a shine to the geek. Since then, the pair had seen a lot of each other...
								</p>
								<p>
									"Honoured passengers," a voice broke in, "InterCity Services is pleased to inform you that our Global President Siegfried Angstrom will be addressing the world in precisely ten seconds. Please stay tuned for this important broadcast."
								</p>
								<p>
									"And she should adjust her skin tone," Stephen added. "She's way too pale."
								</p>
