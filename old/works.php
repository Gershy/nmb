								</p>
								<p>
									But he often wondered: was this his future? He would rescue books and keep them safe for people who had no interest in them? He would master languages that were no longer spoken and study subjects that had long been forgotten? The Repository was a graveyard of ideas. Was this where he wanted to pass his days...?
