								<p>
									"Who else?"
								</p>
								<p>
									"He seems okay. He's arrogant, like most kibbutzniks, but very smart. I also like that he's into jazz: it says a lot of his favour. I still can't believe Rachel's getting married. And you? What do think?"
								</p>
								<p>
									"He's seems decent. I'm surprised she fell for a techno geek. I saw her marrying the poet type, you know, someone who works for Tnuva cheese and writes poems about children praying as tanks roar by... His floating eye would drive me crazy."
								</p>
								<p>
									"It makes him look shifty," Avi agreed, "like a zombie in a bad horror movie."
								</p>
								<p>
									His brother laughed and Avi almost glowed. Dan was older by only five years, but he often acted a lot like a father; since their dad's return to Toronto four years back, Dan had tried to fill his role. Because he'd picked up Hebrew quickly and adapted to Israeli ways at school, he'd helped their mom with the stuff their dad should have seen to: the mortgage, insurance, and other such details. Rachel was the oldest but they relied on Dan.
								</p>
								<p>
									His father. That reminded him.
								</p>
								<p>
									"Abba hasn't phoned. Do you think he forgot?"
								</p>
								<p>
									"That it's your birthday? Nah. He always remembers. He'll phone soon, I guarantee."
								</p>
								<p>
									"It's too bad he couldn't come for Pesach."
								</p>
								<p>
									"His work keeps him busy. It wasn't his fault."
								</p>
								<p>
									Dan said this firmly. He hated even the slightest hint that their father had been wrong to go back to Toronto. The way he saw it, their dad had wanted to stay but hadn't been able to earn a good living. By returning to Canada and his job as a lawyer, he could pay the family's expenses and keep them on in Israel. So he was self-sacrificing, not a failure or quitter. Avi had his own take on his dad's decision, but it wasn't one he could share with Dan, unless he wanted a yelling match.
								</p>
								<p>
									"Ima was crying when she served the cake," he said, steering the talk to safer ground, "Sometimes she's too sentimental."
								</p>
								<p>
									"You're turning fifteen," Dan said. "Your childhood has ended."
								</p>
								<p>
									"I thought that happens at thirteen for us Jews."
								</p>
								<p>
									"You know what I mean. You're filling out. In another three years you'll be entering Tzahal. Your teachers will treat you differently now, they'll show you new stuff and get you ready for the army. You'll even learn how to handle a gun."
								</p>
								<p>
									Avi nodded. It made him faintly nauseous to think that soon he too would be part of the army. And Dan was right: the 'grooming' had started. Their phys ed teacher had been yelling all year that they had to be in top condition; in geography they were learning to use a map and compass. And teachers were discussing their army service, in low, serious tones, the way one would engage men, not adolescents.
								</p>
								<p>
									He had to keep his hands from shaking.
								</p>
								<p>
									"I love this rain," Dan said, flicking a butt into the air, "but we should return before Dror thinks we're avoiding his eye."
								</p>
								<p>
									Placing his arm around his brother, Dan steered him inside. Although the apartment was small and cold in winter and the plumbing was faulty, it was a cozy space overall. The living-room's furniture was comfortable, and there were shelves with books on all kinds of subjects. The walls were covered with gorgeous landscapes that Avi's mother Shosh had painted. Many depicted winter scenes: Shosh was convinced they would cool things down in summer, if only psychologically. The TV stood against one wall; Rachel and Shosh were watching the news, along with Rachel's fiancé Dror.
								</p>
								<p>
									"There was an incident by the wall," Dror spoke. A muscular guy, he was dressed in jeans and a t-shirt stamped with the smiling image of Bob Marley. He was warm and friendly and spoke with self-assurance, as if he knew better than everyone else – a typical Israeli. "They say a couple of shots were fired."
								</p>
								<p>
									"There was an incident," Dan spoke tartly. He didn't like discussing the day's events, in case the wrong emotions were provoked. "It happened at the Kalandiyah checkpoint. There was a family with a very sick child – he had leukemia and needed a blood transfusion. They had to get to a hospital fast but lacked the right permit and begged the guard to let them pass."
								</p>
								<p>
									"So what happened?" Rachel asked. With her chestnut hair and bony features, she looked the spitting image of Dan. "I'll bet the guard didn't lift a finger."
								</p>
								<p>
									"He had his orders," Dan replied, "And you have to remember the line-ups are huge. Everyone's on edge, the guards and civilians."
								</p>
								<p>
									"How did it end?" Shosh was eyeing Dan. His stories always drew her in, since they gave her an insight into events that would otherwise pass unseen.
								</p>
								<p>
									"The father got so angry that he threw a punch. He was quickly subdued but other Arabs moved in and three warning shots were fired. I arrived five minutes later. We summoned an ambulance and let the boy through. As soon as he was gone, the crowd calmed down. The father's being detained, but he should get off with a warning."
								</p>
								<p>
									"Those checkpoints are monstrous," Rachel cried. "Those concrete towers are soul-destroying, not to mention the turnstiles and walls of steel. We treat the Arabs no better than cattle."
								</p>
								<p>
									"What about that bomb last week?" Shosh interjected, "It killed nine people in Tel Aviv and injured dozens more. Without the wall I couldn't sleep at night. I know it's not perfect but I'm glad it's there."
								</p>
								<p>
									"That's why we're pushing a million Arabs around? So that you can sleep soundly at night...?"
								</p>
								<p>
									The debate raged on. Although the women appealed to Dan and Dror, the men kept their distance. It was always like this: while the males might argue among themselves, they seldom spoke their minds when women were present. Avi suspected they knew things that the women weren't exposed to: facts and hard-won information that would pull the plug on any such discussion.
								</p>
								<p>
									And as always, he had nothing to say. He'd heard them argue so many times before that there wasn't anything new to add. And their talk made him very uneasy, especially because he'd turned fifteen. Something was over, something precious was gone. It had been slipping for the last three years but, as Dan himself had hinted, it had disappeared completely that day. The age fifteen seemed to make all the difference. The old Avi Greenbaum, who built forts out of pillows, who played with Lego for hours on end, who constructed model planes and watched hours of cartoons, this Avi Greenbaum was a thing of the past.
								</p>
								<p>
									What would replace him?
								</p>
								<p>
									"Enough politics," Dror finally broke in. "Let's discuss something else, like where we'll live after we get married. Rachel wants to stay here, but I would like to live in Tel Aviv."
								</p>
								<p>
									"Why would you want to live in Tel Aviv?" Shosh asked. "It's full of crazy drivers."
								</p>
								<p>
									"As opposed to Jerusalem where people are nuts with religion," Dan observed.
								</p>
								<p>
									"Exactly," Dror agreed. "I would like to live somewhere normal for once."
								</p>
								<p>
									"Then you'll have to leave the country," Dan joked.
								</p>
								<p>
									"We're not moving from Jerusalem," Rachel insisted, "I want to be able to walk to the Kotel."
								</p>
								<p>
									"But you never go to the Kotel!"
								</p>
								<p>
									"That doesn't matter. It makes me feel safe to know it's nearby."
								</p>
								<p>
									"I know what you mean," Shosh said approvingly, "It makes me feel secure as well..."
								</p>
								<p>
									These comments led a new discussion, about the value of sticking to the old traditions even if you weren't that religious. Avi had heard these arguments too and found them just as tedious. Why couldn't they discuss something positive, like music? Thank God the telephone intruded just then. And he guessed who it was before he picked up the receiver. His dad had remembered, as Dan had promised he would.
								</p>
								<p>
									"Happy birthday, Avi."
								</p>
								<p>
									"So you did remember!"
								</p>
								<p>
									"Have I ever forgotten? My keys I forget, or my secretary's name, but birthdays, never. What's it like to be fifteen?"
								</p>
								<p>
									"It's the same as being fourteen but I get to stay up later." If only it were as simple as that. "I'm sorry you're not here. And we missed you at Pesach."
								</p>
								<p>
									"Things didn't work out. It's too bad too because the weather has been awful.
								</p>
								<p>
									It's snowing here, even as I speak. We had a blizzard last week and it's storming again. Why people ever settled here is way beyond me."
								</p>
								<p>
									As he went on to say he would be there for the wedding, Avi pictured him in his downtown condo, with snow burying the city alive and the traffic on Yonge Street brought to a standstill. Every tree would be coated in white, the city's sounds would be beautifully muted and everything would move in slow motion. He realized with a pang that he missed the winter. Far from being inconvenient, the snow was padding against the... crap out there. And his father knew as much. As sorry as he was to live far away, Avi could hear his relief when he complained.
								</p>
								<p>
									Why was he relieved? For the same reason Avi regretted that he'd turned fifteen.
								</p>
								<p>
									"... Are you going to that music festival in England?"
								</p>
								<p>
									"As far as I know," Avi said, returning to the conversation. "We were told to have a valid passport ready. That means it's still on."
								</p>
								<p>
									"Great. And have you opened your present?"
								</p>
								<p>
									"I have. Thanks a million. The Artie Shaw music book was just what I wanted."
								</p>
								<p>
									"Use it well. Your mom emailed me that you'll be playing soccer against an Arab team. Is that true?"
								</p>
								<p>
									"Yeah. Our first match starts in a couple of days."
								</p>
								<p>
									"I ask because I ran into a friend last week. Do you remember Phil Matthews? He's a freelance reporter?"
								</p>
								<p>
									"I don't think so."
								</p>
								<p>
									"Well, he's looking for a story with a Jewish-Arab angle. I mentioned something about your match and he'd like to interview a player from each team. Interested?"
								</p>
								<p>
									"I guess."
								</p>
								<p>
									"Great. I'll tell him. Now pass me to your mother. And remember: you'll see me in just under four weeks."
								</p>
								<p>
									Bidding him goodbye, Avi handed the phone over. For a moment he felt an emptiness engulf him. It was the thought of his dad living far away, and his brother's fatigue, and the news on TV, and the fact another year had passed and soon, very soon, he would have to do what all men do. He closed his eyes. The feeling wouldn't pass. To stabilize himself, he sat next to Dan who, sensing his mood, passed some pistachios over.
								</p>
								<p>
									"How's Abba?" he asked.
								</p>
								<p>
									"The same. It's snowing in Toronto."
								</p>
								<p>
									"I'd love to see it snow," Dan said.
								</p>
								<p>
									The siblings began discussing the stuff they missed most in Canada. Between them they mentioned diners, hockey, thunderstorms, forests, lakes, and summer camping. Rachel mentioned the trips they'd taken, and Dan agreed he missed them most, jumping in the car and heading down a highway, to PEI, BC or south of the border, on and on, seemingly forever.
								</p>
								<p>
									Dror inquired about PEI, and Dan and Rachel clued him in. Still reeling from his empty feeling, Avi grabbed some nuts and passed outside.
								</p>
								<p>
									It was beautiful out. The rain was like a whisper in his ear. Water was streaming in drops from a drainpipe and the tempo reminded him of Artie Shaw's "Temptation". Glancing into the street below, he spied a cat clawing about in a dumpster. Thinking it might like a pistachio nut, he cracked one open and tossed it down. It only made the creature jump and, with a growl of suspicion, it ran into the shadows, leaving its cache of chicken behind.
								</p>
								<p>
									Avi smiled and caught a raindrop on his tongue. He hoped the rain would never let up. <span class="star-break"></span>
									It was 2:00 a.m. when he finally stretched out. His shelves were empty and the walls were bare. His model planes and Lego set had been packed into boxes and stuffed away. He hadn't touched these things in ages but, until that evening, had wanted them around. He still wanted them close at hand but... enough was enough. They had to go. If they weren't staring him in the face each day, it would probably be easier to do what men do.
								</p>
								<p>
									When he turned the light off in his room, he felt he was saying good-bye to an old trusted friend.
								</p>
								
